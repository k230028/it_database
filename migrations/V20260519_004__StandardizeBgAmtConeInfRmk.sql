-- 3) DOC_CONE(BLOB) → DOC_INF(CLOB)
--    대상: TAAABB_BGDOCM, TAAABB_BGDOCL
--    BLOB → CLOB 변환은 DBMS_LOB.CONVERTTOCLOB 사용 (PL/SQL 블록).
