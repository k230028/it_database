ALTER TABLE ITPAPP.TAAABB_BGDOCM RENAME COLUMN DOC_CONE TO DOC_CONE_BAK;
ALTER TABLE ITPAPP.TAAABB_BGDOCM ADD (DOC_INF CLOB);
DECLARE
    v_dest_offset  INTEGER;
    v_src_offset   INTEGER;
    v_lang_context INTEGER := DBMS_LOB.DEFAULT_LANG_CTX;
    v_warning      INTEGER;
    v_clob         CLOB;
BEGIN
    FOR r IN (SELECT ROWID rid, DOC_CONE_BAK FROM ITPAPP.TAAABB_BGDOCM WHERE DOC_CONE_BAK IS NOT NULL) LOOP
        DBMS_LOB.CREATETEMPORARY(v_clob, TRUE);
        v_dest_offset := 1;
        v_src_offset  := 1;
        DBMS_LOB.CONVERTTOCLOB(v_clob, r.DOC_CONE_BAK, DBMS_LOB.LOBMAXSIZE,
                               v_dest_offset, v_src_offset,
                               NLS_CHARSET_ID('AL32UTF8'), v_lang_context, v_warning);
        UPDATE ITPAPP.TAAABB_BGDOCM SET DOC_INF = v_clob WHERE ROWID = r.rid;
        DBMS_LOB.FREETEMPORARY(v_clob);
    END LOOP;
END;
/
COMMIT;
ALTER TABLE ITPAPP.TAAABB_BGDOCM DROP COLUMN DOC_CONE_BAK;
COMMENT ON COLUMN ITPAPP.TAAABB_BGDOCM.DOC_INF IS '문서정보';

