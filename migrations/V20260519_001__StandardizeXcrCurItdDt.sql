-- 컬럼 표준화
--   XCR    : NUMBER(9,4)
--   CUR    → CUR_C : VARCHAR2(3)
--   ITD_DT : VARCHAR2(8)

-- ============================================================
-- 1) XCR : NUMBER(9,4)
--    대상: TAAABB_BITEMM, TAAABB_BITEML, TAAABB_BCOSTM, TAAABB_BCOSTL,
--         TAAABB_BTERMM, TAAABB_BTERML
--
--    ORA-01440 회피: Oracle 은 데이터가 있는 NUMBER 컬럼의 정밀도/자리수 축소를
--    허용하지 않으므로 "신규 컬럼 추가 → 데이터 복사 → 원본 DROP → RENAME" 패턴 사용.
--    기존 환율값이 NUMBER(9,4) 범위(99999.9999)를 초과하면 복사 시 오류 발생.
-- ============================================================

-- 1-1) TAAABB_BITEMM
ALTER TABLE ITPAPP.TAAABB_BITEMM ADD (XCR_NEW NUMBER(9,4));
UPDATE ITPAPP.TAAABB_BITEMM SET XCR_NEW = XCR;
COMMIT;
ALTER TABLE ITPAPP.TAAABB_BITEMM DROP COLUMN XCR;
ALTER TABLE ITPAPP.TAAABB_BITEMM RENAME COLUMN XCR_NEW TO XCR;
COMMENT ON COLUMN ITPAPP.TAAABB_BITEMM.XCR IS '환율';

-- 1-2) TAAABB_BITEML
ALTER TABLE ITPAPP.TAAABB_BITEML ADD (XCR_NEW NUMBER(9,4));
