-- ============================================================
-- 2) CUR → CUR_C, VARCHAR2(3)
--    대상: TAAABB_BITEMM, TAAABB_BITEML, TAAABB_BCOSTM, TAAABB_BCOSTL,
--         TAAABB_BTERMM, TAAABB_BTERML
-- ============================================================
ALTER TABLE ITPAPP.TAAABB_BITEMM   RENAME COLUMN CUR TO CUR_C;
ALTER TABLE ITPAPP.TAAABB_BITEML   RENAME COLUMN CUR TO CUR_C;
ALTER TABLE ITPAPP.TAAABB_BCOSTM   RENAME COLUMN CUR TO CUR_C;
ALTER TABLE ITPAPP.TAAABB_BCOSTL   RENAME COLUMN CUR TO CUR_C;
ALTER TABLE ITPAPP.TAAABB_BTERMM   RENAME COLUMN CUR TO CUR_C;
ALTER TABLE ITPAPP.TAAABB_BTERML   RENAME COLUMN CUR TO CUR_C;

UPDATE ITPAPP.TAAABB_BITEMM   SET CUR_C = SUBSTR(CUR_C, 1, 3) WHERE LENGTH(CUR_C) > 3;
UPDATE ITPAPP.TAAABB_BITEML   SET CUR_C = SUBSTR(CUR_C, 1, 3) WHERE LENGTH(CUR_C) > 3;
UPDATE ITPAPP.TAAABB_BCOSTM   SET CUR_C = SUBSTR(CUR_C, 1, 3) WHERE LENGTH(CUR_C) > 3;
UPDATE ITPAPP.TAAABB_BCOSTL   SET CUR_C = SUBSTR(CUR_C, 1, 3) WHERE LENGTH(CUR_C) > 3;
UPDATE ITPAPP.TAAABB_BTERMM   SET CUR_C = SUBSTR(CUR_C, 1, 3) WHERE LENGTH(CUR_C) > 3;
UPDATE ITPAPP.TAAABB_BTERML   SET CUR_C = SUBSTR(CUR_C, 1, 3) WHERE LENGTH(CUR_C) > 3;
COMMIT;

ALTER TABLE ITPAPP.TAAABB_BITEMM   MODIFY (CUR_C VARCHAR2(3));
ALTER TABLE ITPAPP.TAAABB_BITEML   MODIFY (CUR_C VARCHAR2(3));
ALTER TABLE ITPAPP.TAAABB_BCOSTM   MODIFY (CUR_C VARCHAR2(3));
ALTER TABLE ITPAPP.TAAABB_BCOSTL   MODIFY (CUR_C VARCHAR2(3));
ALTER TABLE ITPAPP.TAAABB_BTERMM   MODIFY (CUR_C VARCHAR2(3));
ALTER TABLE ITPAPP.TAAABB_BTERML   MODIFY (CUR_C VARCHAR2(3));

COMMENT ON COLUMN ITPAPP.TAAABB_BITEMM.CUR_C IS '통화코드';
COMMENT ON COLUMN ITPAPP.TAAABB_BITEML.CUR_C IS '통화코드';
COMMENT ON COLUMN ITPAPP.TAAABB_BCOSTM.CUR_C IS '통화코드';
COMMENT ON COLUMN ITPAPP.TAAABB_BCOSTL.CUR_C IS '통화코드';
COMMENT ON COLUMN ITPAPP.TAAABB_BTERMM.CUR_C IS '통화코드';
COMMENT ON COLUMN ITPAPP.TAAABB_BTERML.CUR_C IS '통화코드';

-- ============================================================
-- 3) ITD_DT : VARCHAR2(8)
--    대상: TAAABB_BITEMM, TAAABB_BITEML
--    기존 데이터(YYYY-MM 등) 8자 초과분 절단
-- ============================================================
