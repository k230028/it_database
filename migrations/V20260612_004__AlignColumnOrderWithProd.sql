-- ============================================================
-- B. DFR_DT는 NULL 허용 유지 (의도적 메타 차이)
--    운영 메타(table.csv)는 NULL여부=N이나, 지급 회차는 지급일자 미확정
--    상태(작성중)로 저장될 수 있어 NULL 허용이 업무상 올바름.
--    당초 본 스크립트가 NOT NULL을 적용했으나 2026-06-12 결정으로 환원.
-- ============================================================
DECLARE
  PROCEDURE allow_null(p_tab IN VARCHAR2) IS
    v_nul VARCHAR2(1);
  BEGIN
    SELECT nullable INTO v_nul FROM all_tab_columns
    WHERE owner = 'ITPOWN' AND table_name = p_tab AND column_name = 'DFR_DT';
    IF v_nul = 'N' THEN
      EXECUTE IMMEDIATE 'ALTER TABLE ITPOWN.' || p_tab || ' MODIFY (DFR_DT NULL)';
      DBMS_OUTPUT.PUT_LINE('NULLABLE ' || p_tab || '.DFR_DT');
    END IF;
  END;
BEGIN
  allow_null('TPRMPP_BPAYTM');
  allow_null('TPRMPP_BPAYTL');
END;
