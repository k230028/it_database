-- 사용법:
--   1) 기존 시퀀스(S_*)는 수동으로 DROP
--   2) 이 파일 실행하여 SEQ_* 신규 생성
--   3) 필요 시 START WITH 값을 운영 데이터에 맞게 조정
--
-- MAXVALUE / CYCLE 결정 규칙 (코드 분석 기반):
--   - 4자리 패딩 + 연도 prefix → MAXVALUE 9999 + CYCLE (연도 차이로 충돌 회피)
--   - 8자리 패딩 + 연도 prefix → MAXVALUE 99999999 + CYCLE
--   - 8자리 패딩, 연도 없음    → MAXVALUE 99999999 + NOCYCLE (충돌 방지)
--   - 28자리 패딩, 연도 없음   → MAXVALUE 10^28-1 + NOCYCLE
--   - Long PK (패딩 없음)      → MAXVALUE 9223372036854775807 (Long.MAX) + NOCYCLE
--   - 22자리 패딩 로그 시퀀스  → MAXVALUE 22 nines + CYCLE (AuditLogIdGenerator 명시)
-- ============================================================================

