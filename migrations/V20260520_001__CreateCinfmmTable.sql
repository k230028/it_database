COMMENT ON COLUMN ITPAPP.TAAABB_CINFMM.LST_CHG_USID IS '최종변경자사번';

-- ── 3. 사용자별 미읽음/최근 알림 조회용 핵심 인덱스 ─────────────────────────
--   드롭다운 목록 + 뱃지 카운트 쿼리가 대다수의 트래픽을 차지하므로
--   (RCV_USID, DEL_YN, RDD_YN, FST_ENR_DTM DESC) 다중 컬럼 인덱스를 둔다.
CREATE INDEX ITPAPP.IX_CINFMM_RCV
    ON ITPAPP.TAAABB_CINFMM (RCV_USID, DEL_YN, RDD_YN, FST_ENR_DTM DESC)
    TABLESPACE USERS;

-- ── 4. 시퀀스 — INF-{yyyy}-%08d (8자리 패딩, 연도 prefix → CYCLE) ───────────
--   결재 SEQ_CAPPLM 패턴과 동일한 범위·CYCLE 설정.
CREATE SEQUENCE ITPAPP.SEQ_CINFMM
    START WITH 1
    INCREMENT BY 1
    MAXVALUE 99999999
    CYCLE
    NOCACHE;

