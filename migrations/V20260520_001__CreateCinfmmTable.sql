COMMENT ON COLUMN ITPAPP.TAAABB_CINFMM.RCV_USID     IS '수신자사번';
COMMENT ON COLUMN ITPAPP.TAAABB_CINFMM.RDD_YN       IS '읽음여부';
COMMENT ON COLUMN ITPAPP.TAAABB_CINFMM.RDD_DTM      IS '읽음일시';
COMMENT ON COLUMN ITPAPP.TAAABB_CINFMM.EAI_SD_TP_C  IS 'EAI발송구분코드';
