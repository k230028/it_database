-- ============================================================================
-- 알림 마스터 테이블 신규 생성
-- ============================================================================
-- 목적:
--   결재요청, 결재결과, 게시판 멘션 등 사내 알림을 1행=1수신자 구조로 적재한다.
--   AppHeader 종 아이콘의 미읽음 뱃지·드롭다운 UI와 결합한다.
--
-- 명명 규칙 (META.md / DOMAIN.md 준수):
--   테이블: TAAABB_{C공통}{INFM 알림}{M 마스터} = TAAABB_CINFMM
--   엔티티: Cinfmm (BaseEntity 상속)
--   채번 형식: INF-{YYYY}-{8자리 시퀀스} (결재 APF-... 패턴 미러)
--
-- 핵심 컬럼:
--   필수(사용자 요구): EAI_SD_TP_C(3), EAI_SD_DTM(DATE), EAI_SD_CONE(4000)
--   알림 본문: INF_TTL(100), INF_CONE(300), INF_LNK_URL(300)
--   수신/읽음: RCV_USID(14), RDD_YN(1), RDD_DTM(DATE)
--
-- 인덱스 설계:
--   IX_CINFMM_RCV : 사용자별 미읽음/최근 알림 조회 핵심 인덱스
--     (RCV_USID, DEL_YN, RDD_YN, FST_ENR_DTM DESC)
-- ============================================================================

-- ── 1. 테이블 생성 ──────────────────────────────────────────────────────────
CREATE TABLE ITPAPP.TAAABB_CINFMM (
    INF_MNG_NO     VARCHAR2(32)    NOT NULL,
    INF_TP_C       VARCHAR2(3)     NOT NULL,
    INF_TTL        VARCHAR2(100),
    INF_CONE       VARCHAR2(300),
    INF_LNK_URL    VARCHAR2(300),
    RCV_USID       VARCHAR2(14)    NOT NULL,
    RDD_YN         VARCHAR2(1)     DEFAULT 'N' NOT NULL,
    RDD_DTM        DATE,
    EAI_SD_TP_C    VARCHAR2(3),
    EAI_SD_DTM     DATE,
    EAI_SD_CONE    VARCHAR2(4000),
    -- BaseEntity 공통 컬럼
    DEL_YN         VARCHAR2(1)     DEFAULT 'N' NOT NULL,
    GUID           VARCHAR2(38),
    GUID_PRG_SNO   NUMBER          DEFAULT 1,
    FST_ENR_DTM    TIMESTAMP(6),
    FST_ENR_USID   VARCHAR2(14),
    LST_CHG_DTM    TIMESTAMP(6),
    LST_CHG_USID   VARCHAR2(14),
    CONSTRAINT PK_CINFMM PRIMARY KEY (INF_MNG_NO) USING INDEX TABLESPACE USERS
)
TABLESPACE USERS;

-- ── 2. 테이블·컬럼 코멘트 (META.md 용어사전 기반 한글 코멘트) ───────────────
COMMENT ON TABLE  ITPAPP.TAAABB_CINFMM              IS '알림 마스터';
