-- =====================================================================
-- 결재상태코드 레거시 1자리 값 백필
-- =====================================================================
-- 배경:
--   V20260603_006 에서 TPRMPP_CDECIM.DCD_STS_C 컬럼 폭은 3자리로 확대했지만,
--   기존 데이터의 1자리 값(0/1/2/3)은 그대로 남을 수 있다.
--   애플리케이션 표준 코드는 001/002/003/004 이므로 조회·결재 처리 전에
--   저장 데이터를 표준값으로 정규화한다.
--
-- 매핑:
--   0 → 001(미결재), 1 → 002(승인), 2 → 003(반려), 3 → 004(회수무효)
--
-- 멱등성:
--   WHERE 조건이 1자리 레거시 값만 대상으로 하므로 재실행해도 추가 변경이 없다.
-- =====================================================================

UPDATE ITPAPP.TPRMPP_CDECIM
   SET DCD_STS_C = CASE TRIM(DCD_STS_C)
       WHEN '0' THEN '001'
       WHEN '1' THEN '002'
       WHEN '2' THEN '003'
       WHEN '3' THEN '004'
       ELSE DCD_STS_C
   END
 WHERE TRIM(DCD_STS_C) IN ('0', '1', '2', '3');
