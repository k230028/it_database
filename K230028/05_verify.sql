SET LINESIZE 200
SET PAGESIZE 100
SET FEEDBACK ON
COL chk    FORMAT A40
COL result FORMAT A40

PROMPT === 20 테이블 존재 (기대=20) ===
SELECT 'TABLES_COUNT' AS chk, COUNT(*) || ' / 20' AS result FROM user_tables
WHERE table_name IN (
  'TPRMPP_BASCTM','TPRMPP_BCHKLM','TPRMPP_BCMMTM','TPRMPP_BEVALM','TPRMPP_BMQNAM',
  'TPRMPP_BPERFM','TPRMPP_BPOVWM','TPRMPP_BPQNAM','TPRMPP_BRSLTM','TPRMPP_BSCHDM',
  'TPRMPP_BASCTL','TPRMPP_BCHKLL','TPRMPP_BCMMTL','TPRMPP_BEVALL','TPRMPP_BMQNAL',
  'TPRMPP_BPERFL','TPRMPP_BPOVWL','TPRMPP_BPQNAL','TPRMPP_BRSLTL','TPRMPP_BSCHDL');

PROMPT === 시퀀스 12개 존재 ===
SELECT 'SEQUENCES_COUNT' AS chk, COUNT(*) || ' / 12' AS result FROM user_sequences
WHERE sequence_name IN (
  'SEQ_BASCTM','SEQ_BCHKLM','SEQ_BMQNAL','SEQ_BBUGTL','SEQ_BCHKLL','SEQ_BCMMTL',
  'SEQ_BEVALL','SEQ_BPERFL','SEQ_BPOVWL','SEQ_BPQNAL','SEQ_BRSLTL','SEQ_BSCHDL'
);

PROMPT === PK 표준 명명 (기대=20: PK_*) ===
SELECT 'PK_NAMES' AS chk, COUNT(*) || ' / 20' AS result FROM user_constraints
WHERE constraint_type='P'
  AND constraint_name LIKE 'PK_%'
  AND table_name IN (
  'TPRMPP_BASCTM','TPRMPP_BCHKLM','TPRMPP_BCMMTM','TPRMPP_BEVALM','TPRMPP_BMQNAM',
  'TPRMPP_BPERFM','TPRMPP_BPOVWM','TPRMPP_BPQNAM','TPRMPP_BRSLTM','TPRMPP_BSCHDM',
  'TPRMPP_BASCTL','TPRMPP_BCHKLL','TPRMPP_BCMMTL','TPRMPP_BEVALL','TPRMPP_BMQNAL',
  'TPRMPP_BPERFL','TPRMPP_BPOVWL','TPRMPP_BPQNAL','TPRMPP_BRSLTL','TPRMPP_BSCHDL');

