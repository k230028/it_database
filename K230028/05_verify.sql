SELECT 'TP_COL_REMAIN'        AS chk, COUNT(*) AS result FROM user_tab_columns WHERE column_name='CHG_TC' AND table_name IN ('TPRMPP_BASCTL','TPRMPP_BCHKLL','TPRMPP_BCMMTL','TPRMPP_BEVALL','TPRMPP_BMQNAL','TPRMPP_BPERFL','TPRMPP_BPOVWL','TPRMPP_BPQNAL','TPRMPP_BRSLTL','TPRMPP_BSCHDL');
